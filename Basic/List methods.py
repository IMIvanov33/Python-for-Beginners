numbers = [5, 2, 1, 5, 7, 4, 3]
numbers.append(50)  # Add 50 to the end of the list
numbers.insert(0, 100)  # Insert 100 at the beginning of the list
numbers.remove(1)  # Remove the first occurrence of 1 from the list
numbers2 = numbers.copy()  # Create a copy of the list
numbers.sort()  # Sort the list in ascending order
print(numbers)  # Print the modified list
print(numbers2)  # Print the copied list
